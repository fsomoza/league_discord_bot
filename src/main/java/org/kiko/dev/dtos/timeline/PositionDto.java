package org.kiko.dev.dtos.timeline;

public class PositionDto {
    private double x;
    private double y;

    // Getters and Setters
    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
